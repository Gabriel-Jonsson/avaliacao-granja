/* Controle de pesagem e mortalidade de aves */

CREATE TABLE TAB_LOTE_AVES (
    ID_LOTE             INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    DESCRICAO           VARCHAR(100)   NOT NULL,
    DATA_ENTRADA        DATE           NOT NULL,
    QUANTIDADE_INICIAL  INTEGER        NOT NULL,
    PESO_MEDIO_GERAL    NUMERIC(10,2),

    CONSTRAINT PK_TAB_LOTE_AVES     PRIMARY KEY (ID_LOTE),
    CONSTRAINT CK_LOTE_QTD_INICIAL  CHECK (QUANTIDADE_INICIAL > 0)
);

CREATE TABLE TAB_PESAGEM (
    ID_PESAGEM          INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ID_LOTE_FK          INTEGER        NOT NULL,
    DATA_PESAGEM        DATE           NOT NULL,
    PESO_MEDIO          NUMERIC(10,2)  NOT NULL,
    QUANTIDADE_PESADA   INTEGER        NOT NULL,

    CONSTRAINT PK_TAB_PESAGEM   PRIMARY KEY (ID_PESAGEM),
    CONSTRAINT FK_PESAGEM_LOTE  FOREIGN KEY (ID_LOTE_FK)
                                REFERENCES TAB_LOTE_AVES (ID_LOTE),
    CONSTRAINT CK_PESAGEM_QTD   CHECK (QUANTIDADE_PESADA > 0),
    CONSTRAINT CK_PESAGEM_PESO  CHECK (PESO_MEDIO > 0)
);

CREATE TABLE TAB_MORTALIDADE (
    ID_MORTALIDADE      INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ID_LOTE_FK          INTEGER        NOT NULL,
    DATA_MORTALIDADE    DATE           NOT NULL,
    QUANTIDADE_MORTA    INTEGER        NOT NULL,
    OBSERVACAO          VARCHAR(255),

    CONSTRAINT PK_TAB_MORTALIDADE   PRIMARY KEY (ID_MORTALIDADE),
    CONSTRAINT FK_MORTALIDADE_LOTE  FOREIGN KEY (ID_LOTE_FK)
                                    REFERENCES TAB_LOTE_AVES (ID_LOTE),
    CONSTRAINT CK_MORTALIDADE_QTD   CHECK (QUANTIDADE_MORTA > 0)
);

COMMIT;
